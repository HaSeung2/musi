package dto;

public class NormalDTO {
public String normalid;
public String normalpw;
public String normalname;
public int normalage;
public String normalphone;
public String normaladdr;
public NormalDTO(String normalid, String normalpw, String normalname, int normalage, String normalphone,
		String normaladdr) {
	this.normalid = normalid;
	this.normalpw = normalpw;
	this.normalname = normalname;
	this.normalage = normalage;
	this.normalphone = normalphone;
	this.normaladdr = normaladdr;
}
}
